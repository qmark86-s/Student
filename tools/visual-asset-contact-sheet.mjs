import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve, sep } from "node:path";

const visual = JSON.parse(readFileSync(resolve("data/visual_assets.json"), "utf8"));
const outputPath = resolve("artifacts/visual-asset-contact-sheet/index.html");

function posPercent(index, total) {
  return total <= 1 ? "0%" : `${(index / (total - 1)) * 100}%`;
}

function itemIndex(item) {
  if (Number.isInteger(item.index)) return item.index;
  if (Number.isInteger(item.frame)) return item.frame;
  return 0;
}

function assetUrl(file) {
  const fromOutput = dirname(outputPath);
  const assetPath = resolve("src/snapshot", file);
  return relative(fromOutput, assetPath).split(sep).join("/");
}

function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function itemLabel(item) {
  return item.title || item.name || item.id;
}

function itemMeta(item) {
  return [item.phase, item.age ? `${item.age}세` : "", item.band, item.type, item.tone].filter(Boolean).join(" / ");
}

function renderCell(atlas, item) {
  const total = atlas.width / atlas.cell;
  const index = itemIndex(item);
  const title = [item.id, itemLabel(item), itemMeta(item), item.gradeTitle].filter(Boolean).join(" · ");
  return `<figure class="asset-cell" title="${escapeHtml(title)}">
    <span class="sprite" style="--sheet:url('${assetUrl(atlas.file)}');--size:${total * 100}% 100%;--x:${posPercent(index, total)};--w:${atlas.cell}px;--h:${atlas.height}px"></span>
    <figcaption><b>${escapeHtml(itemLabel(item))}</b><small>${escapeHtml(itemMeta(item) || `#${index}`)}</small></figcaption>
  </figure>`;
}

function renderAtlas(atlas) {
  return `<section class="atlas">
    <header>
      <div>
        <h2>${atlas.id}</h2>
        <p>${atlas.file} · ${atlas.width}x${atlas.height} · cell ${atlas.cell} · ${atlas.items.length} items</p>
      </div>
      <strong>${atlas.token}</strong>
    </header>
    <div class="grid">${atlas.items.map((item) => renderCell(atlas, item)).join("\n")}</div>
  </section>`;
}

function renderBackground(background) {
  return `<section class="atlas">
    <header>
      <div>
        <h2>${background.id}</h2>
        <p>${background.file} · ${background.width}x${background.height} · ${background.items.length} stage mappings</p>
      </div>
      <strong>${background.token}</strong>
    </header>
    <img class="background-preview" src="${assetUrl(background.file)}" alt="${background.id}" />
  </section>`;
}

const html = `<!doctype html>
<html lang="ko">
<head>
  <meta charset="utf-8" />
  <meta name="viewport" content="width=device-width, initial-scale=1" />
  <title>Visual Asset Contact Sheet</title>
  <style>
    :root{color:#202938;background:#edf2f7;font-family:Inter,Pretendard,Noto Sans KR,system-ui,sans-serif}
    *{box-sizing:border-box}
    body{margin:0;padding:24px}
    main{max-width:1400px;margin:0 auto;display:grid;gap:18px}
    h1,h2,p,figure{margin:0}
    .summary{background:#202938;color:#f8fafc;border-radius:8px;padding:18px;display:grid;gap:6px}
    .summary p{color:#cbd5e1;font-size:14px}
    .atlas{background:#fff;border:1px solid #d8e0eb;border-radius:8px;padding:14px;display:grid;gap:12px}
    .atlas header{display:flex;justify-content:space-between;align-items:flex-start;gap:14px}
    .atlas h2{font-size:18px}
    .atlas p,.atlas strong{color:#667085;font-size:12px}
    .atlas strong{white-space:nowrap;background:#eef2f7;border-radius:6px;padding:5px 7px}
    .grid{display:grid;grid-template-columns:repeat(auto-fill,minmax(108px,1fr));gap:10px}
    .asset-cell{background:#f8fafc;border:1px solid #e2e8f0;border-radius:8px;padding:8px;display:grid;justify-items:center;gap:6px;min-width:0}
    .sprite{width:var(--w);height:var(--h);max-width:96px;max-height:96px;background-image:var(--sheet);background-size:var(--size);background-position:var(--x) 0;background-repeat:no-repeat;image-rendering:pixelated;filter:drop-shadow(3px 4px 0 #00000024)}
    .background-preview{width:100%;max-height:240px;object-fit:cover;object-position:left center;border-radius:8px;border:1px solid #e2e8f0;background:#0f172a}
    figcaption{width:100%;display:grid;gap:2px;text-align:center;min-width:0}
    figcaption b,figcaption small{overflow:hidden;text-overflow:ellipsis;white-space:nowrap}
    figcaption b{font-size:11px}
    figcaption small{color:#667085;font-size:10px}
  </style>
</head>
<body>
  <main>
    <section class="summary">
      <h1>Visual Asset Contact Sheet</h1>
      <p>${visual.style} · generated by ${visual.generatedBy} · ${visual.atlases.length} atlases</p>
    </section>
    ${visual.atlases.map(renderAtlas).join("\n")}
    ${(visual.backgrounds ?? []).map(renderBackground).join("\n")}
  </main>
</body>
</html>
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, html, "utf8");
console.log(`VISUAL_ASSET_CONTACT_SHEET_OK ${outputPath}`);
